<template>
  <div>
    <h1>Todoリスト</h1>
    <table>
      <tr>
        <th>連番</th>
        <th>内容</th>
        <th>完了</th>
        <th>追加日</th>
      </tr>
      <tr v-for="(todo, i) in todos" :key="i">
        <td>
          {{ i + 1 }}
        </td>
        <td>
          {{ todo.title }}
        </td>
        <td>
          {{ todo.done }}
        </td>
        <td>
          {{ format(todo.created_at) }}
        </td>
      </tr>

    </table>
  </div>
</template>

<script>
export default {
  data() {
    return {
      todos: [
        {
          title: '買い物',
          done: false,
          created_at: new Date()
        },
        {
          title: 'Jsの勉強',
          done: false,
          created_at: new Date()
        },
      ]
    }
  },
  methods: {
    format(date) {
      return date.getFullYear()
        + '/' + (date.getMonth() + 1)
        + '/' + date.getDate()
        + ' ' + date.getHours()
        + ':' + date.getMinutes();
    }
  }

}
</script>

<style>
th, td {
  border: 1px solid #ddd;
}
</style>
